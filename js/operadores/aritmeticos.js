
/*
Operator	Description
+	Addition
-	Subtraction
*	Multiplication
/	Division
**	Exponentiation
%	Modulus (Division Remainder)
++	Increment
--	Decrement
*/

let a = 5;
let b = 2;

// +

let suma = a + b;
console.log(suma) // 7

// - 

let resta = a - b;
console.log(resta); // 3

// * 

let multiplicacion = a * b;
console.log(multiplicacion); // 10

// (/)

let division = a / b;
console.log(division) // 2.5

// ** 

let exponente = a ** b;
console.log(exponente); // 25

// %

let modul = a % b; 
console.log(modul); // 1 

// ++

let increment = a++;
console.log(increment); // 6

// --
let decrement = a--;
console.log(decrement); // 4


