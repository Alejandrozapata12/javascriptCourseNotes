/*
La directiva era nueva en la versión 5 de ECMAScript."use strict"

Define que el código JavaScript debe ejecutarse en "modo estricto".

No es una afirmación. Es una expresión literal, ignorada por versiones anteriores de JavaScript.

*/

/*Declaración del modo estricto
El modo estricto se declara añadiendo "use strict"; al inicio de una script o función.

Declarado al principio de un script, tiene alcance global (todo el código en el script se ejecutará en modo estricto):
*/
// Ejemplo
"use strict";
x = 3.14;       // This will cause an error because x is not declared